import { HTTP_INTERCEPTORS, HttpClient } from '@angular/common/http';
import {
  HttpClientTestingModule,
  HttpTestingController
} from '@angular/common/http/testing';
import { TestBed } from '@angular/core/testing';

import { ApiInterceptor } from './api-interceptor';

describe('ApiInterceptor', () => {
  let httpClient: HttpClient;
  let httpMock: HttpTestingController;

  beforeEach(() => {
    TestBed.configureTestingModule({
      imports: [HttpClientTestingModule],
      providers: [
        {
          provide: HTTP_INTERCEPTORS,
          useClass: ApiInterceptor,
          multi: true
        }
      ]
    });

    httpClient = TestBed.inject(HttpClient);
    httpMock = TestBed.inject(HttpTestingController);
  });

  afterEach(() => {
    // Make sure no extra HTTP requests were made.
    httpMock.verify();
  });

  it('should prepend the backend URL to request path', function () {
    let result: any; // eslint-disable-line @typescript-eslint/no-explicit-any
    httpClient.get('/foo/bar').subscribe((res) => (result = res));

    httpMock.expectOne('./data/foo/bar').flush('foo');

    expect(result).toEqual('foo');
  });
});
